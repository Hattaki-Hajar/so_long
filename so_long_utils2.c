void	check_borders(char	**map, int x, int y)
{
	int	i;
	int	j;

	i = 0;
	while (map[1])
	{
		if (map[1][i] != '1')
			exit(3);
		i++;
	}
	i = 0;
	while (map[y - 1])
	{
		if (map[y - 1][i] != '1')
			exit(3);
		i++;
	}
	i = 0;
	while (i < y)
	{
		if (map[i][0] != '1' || map[i][x - 1] != '1')
			exit(4);
		i++;
	}
}
